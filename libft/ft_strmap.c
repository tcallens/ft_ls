#include "libft.h"

char	*ft_strmap(char const *s, char (*f)(char))
{
	char	*str;
	int		a;

	a = 0;
	if (!s)
		return (NULL);
	str = (char *)malloc(sizeof(char) * ft_strlen(s) + 1);
	if (str == NULL)
		return (NULL);
	while (s[a])
	{
		str[a] = f(s[a]);
		a++;
	}
	str[a] = '\0';
	return (str);
}
