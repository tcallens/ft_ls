#include "libft.h"

void	*ft_memcpy(void *str1, const void *str2, size_t n)
{
	int a;

	a = 0;
	while (n--)
	{
		((char *)str1)[a] = ((char *)str2)[a];
		a++;
	}
	return (str1);
}
